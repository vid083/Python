''' Given two positive integers A and B, find GCD of A and B.
Example 1:

Input: A = 3, B = 6
Output: 3
Explanation: GCD of 3 and 6 is 3
'''
import math
class Solution:
    def gcd(self, A, B):
        return math.gcd(A,B)

